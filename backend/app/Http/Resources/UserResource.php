<?php

namespace App\Http\Resources;

use Illuminate\Http\Request;
use Illuminate\Http\Resources\Json\JsonResource;

class UserResource extends JsonResource
{
    /**
     * Transform the resource into an array.
     *
     * @return array<string, mixed>
     */
    public function toArray(Request $request): array
    {
        return [
            'id' => $this->id,
            'name' => $this->name,
            'email' => $this->email,
            // You might want to include projects or tasks if needed,
            // but usually, you'd fetch those via separate endpoints
            // or specific nested resources.
            // 'projects_created' => ProjectResource::collection($this->whenLoaded('projects')),
            // 'tasks_created' => TaskResource::collection($this->whenLoaded('tasks')),
            'created_at' => $this->created_at->format('Y-m-d H:i:s'),
            'updated_at' => $this->updated_at->format('Y-m-d H:i:s'),
        ];
    }
}